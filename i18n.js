let _currentLang = localStorage.getItem('fw_lang') || 'ca';

function t(key) {
  const dict = TRANSLATIONS[_currentLang] || TRANSLATIONS.ca;
  const val = dict[key];
  if (val !== undefined) return val;
  const ca = TRANSLATIONS.ca[key];
  return ca !== undefined ? ca : key;
}
window.t = t;

function getLang() { return _currentLang; }
window.getLang = getLang;

// â”€â”€ Aplicar idioma â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
function applyLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  _currentLang = lang;
  localStorage.setItem('fw_lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = t(el.dataset.i18n);
    if (v) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const v = t(el.dataset.i18nPh);
    if (v) el.placeholder = v;
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}
window.applyLang = applyLang;

// â”€â”€ Selector de llengua â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
function createLangSelector() {
  if (document.getElementById('langSelector')) return;
  const sel = document.createElement('div');
  sel.id = 'langSelector';
  ['ca','es','en'].forEach(lang => {
    const btn = document.createElement('button');
    btn.className = 'lang-btn' + (lang === _currentLang ? ' active' : '');
    btn.dataset.lang = lang;
    btn.textContent = lang.toUpperCase();
    btn.addEventListener('click', () => applyLang(lang));
    sel.appendChild(btn);
  });
  document.body.appendChild(sel);
}

